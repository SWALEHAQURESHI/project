
public class Switchcase {
public static void main(String[] args) {
	int i =300;
	switch(i) {
	case 100:{
		System.out.println("value is hundred");
	}
	case 200:{
		System.out.println("value is two hundred");
	}
	default:{
		System.out.println(" the value is not valid ");
		
	}
	}
}
}
